#!/bin/bash
# ==============================================================================
# 01-stdio.sh — 标准输入输出 & 重定向 示例脚本
# ==============================================================================
# 用法: bash 01-stdio.sh
# 这个脚本演示了 stdin/stdout/stderr 的概念以及各种重定向用法。
# 运行前建议先创建一个临时目录，避免污染当前工作目录。
# ==============================================================================

set -e  # 遇到错误立即退出（方便演示时发现意外问题）

echo "=========================================="
echo "01-stdio.sh: 标准输入输出与重定向演示"
echo "=========================================="
echo ""

# ---- 创建临时演示目录 ----
DEMO_DIR="/tmp/linux-stdio-demo-$$"
mkdir -p "$DEMO_DIR"
cd "$DEMO_DIR"
echo "[INFO] 演示目录: $DEMO_DIR"
echo ""

# ============================================================
# 1. 演示 stdout：正常输出
# ============================================================
echo "--- 1. stdout 示例: date 命令的输出默认到屏幕 ---"
date
echo "上面这行日期出现在屏幕上，因为它走的是 stdout（标准输出）"
echo ""

# ============================================================
# 2. 演示 stderr：错误输出
# ============================================================
echo "--- 2. stderr 示例: ls 一个不存在的目录 ---"
echo "下面这行错误提示也出现在屏幕上，因为它走的是 stderr（标准错误）"
ls /this_directory_does_not_exist_xyz 2>&1 || true
echo ""

# ============================================================
# 3. 演示 > 重定向 stdout（覆盖写入）
# ============================================================
echo "--- 3. > 重定向 stdout（覆盖写入）---"
echo "第一次写入" > demo_overwrite.txt
echo "文件 demo_overwrite.txt 的内容是:"
cat demo_overwrite.txt
echo ""

echo "第二次写入" > demo_overwrite.txt
echo "再次用 > 写入后，demo_overwrite.txt 的内容是:"
cat demo_overwrite.txt
echo "（注意：第一次写入的内容已经被覆盖了！）"
echo ""

# ============================================================
# 4. 演示 >> 重定向 stdout（追加写入）
# ============================================================
echo "--- 4. >> 重定向 stdout（追加写入）---"
echo "第一行" >> demo_append.txt
echo "第二行" >> demo_append.txt
echo "第三行" >> demo_append.txt
echo "demo_append.txt 的内容是:"
cat demo_append.txt
echo "（注意：三行都在，没有被覆盖）"
echo ""

# ============================================================
# 5. 演示 2> 重定向 stderr
# ============================================================
echo "--- 5. 2> 重定向 stderr ---"
ls /this_dir_does_not_exist 2> demo_stderr.txt || true
echo "错误信息被重定向到了 demo_stderr.txt，内容如下:"
cat demo_stderr.txt
echo "（注意：屏幕上看不到错误信息了，它被存进了文件）"
echo ""

# ============================================================
# 6. 演示分开保存 stdout 和 stderr
# ============================================================
echo "--- 6. 分开保存 stdout 和 stderr ---"
ls /home /this_dir_does_not_exist > demo_ok.txt 2> demo_err.txt || true
echo "正常输出 (demo_ok.txt):"
cat demo_ok.txt
echo ""
echo "错误输出 (demo_err.txt):"
cat demo_err.txt
echo ""

# ============================================================
# 7. 演示 &> 合流（stdout + stderr 同文件）
# ============================================================
echo "--- 7. &> 合流（stdout + stderr 到同一个文件）---"
ls /home /this_dir_does_not_exist &> demo_combined.txt || true
echo "合流文件 demo_combined.txt 的内容:"
cat demo_combined.txt
echo "（正常信息和错误信息都在同一个文件里）"
echo ""

# ============================================================
# 8. 演示 /dev/null 丢弃输出
# ============================================================
echo "--- 8. /dev/null 丢弃输出 ---"
echo "执行 ls /home /bad_dir &> /dev/null —— 屏幕上将什么都看不到"
ls /home /this_dir_does_not_exist &> /dev/null || true
echo "看到了吗？上面没有输出任何东西，全被 /dev/null 吃掉了。"
echo ""

# ============================================================
# 9. 演示 < 重定向 stdin
# ============================================================
echo "--- 9. < 重定向 stdin ---"
echo "创建一个测试文件..."
echo -e "apple\nbanana\ncherry\ndate" > fruits.txt
echo "用 sort < fruits.txt 从 stdin 读取并排序:"
sort < fruits.txt
echo ""

echo "用 wc < fruits.txt 统计行数、单词数、字节数:"
wc < fruits.txt
echo ""

# ============================================================
# 10. 演示 heredoc
# ============================================================
echo "--- 10. heredoc 示例（<<）---"
cat << EOF > letter.txt
亲爱的用户:

这是用 heredoc 自动生成的信件。
你可以在这里写任意多行文字，
直到遇到结束标记 EOF 为止。

祝好,
Linux 教程
EOF

echo "letter.txt 的内容:"
cat letter.txt
echo ""

# ============================================================
# 11. 演示 &> /dev/null 的实用场景
# ============================================================
echo "--- 11. 实用场景: 只关心命令是否成功 ---"
if ping -c 1 127.0.0.1 > /dev/null 2>&1; then
    echo "ping 本地回环地址成功（输出被丢弃了，只显示结果判断）"
else
    echo "ping 本地回环地址失败"
fi
echo ""

# ============================================================
# 12. 演示重定向顺序的重要性
# ============================================================
echo "--- 12. 重定向顺序的重要性 ---"
echo "演示: 2>&1 的位置不同，结果不同"

echo "正确写法 (ls /bad > ok.txt 2>&1):"
ls /bad_dir_for_demo > order_ok.txt 2>&1 || true
echo "  order_ok.txt 内容: $(cat order_ok.txt)"

echo "错误写法 (ls /bad 2>&1 > order_bad.txt):"
ls /bad_dir_for_demo 2>&1 > order_bad.txt || true
echo "  你会在屏幕上看到错误信息，因为 2>&1 在 > 之前执行"
echo "  此时 stdout 还是屏幕，所以 stderr 也被导向了屏幕"
echo ""

# ============================================================
# 13. 清场前的总结
# ============================================================
echo "=========================================="
echo "演示完成！所有临时文件在: $DEMO_DIR"
echo "你可以手动删除: rm -rf $DEMO_DIR"
echo "=========================================="
