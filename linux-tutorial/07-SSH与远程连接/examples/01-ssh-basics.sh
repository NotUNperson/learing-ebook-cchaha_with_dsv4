#!/bin/bash
# ========================================
# 01-ssh-basics.sh — SSH 基础知识演示
# ========================================
# 功能：演示 SSH 的安装检查、版本查看、
#        加密概念类比说明、与 Telnet 的对比
# 用法：./01-ssh-basics.sh
# 注意：本脚本仅用于本地演示，不建立实际连接
# ========================================

echo "==========================================="
echo "  SSH 基础概念演示"
echo "==========================================="
echo ""

# --------------------------------------------------
# 一、检查 SSH 是否已安装
# --------------------------------------------------
echo "--- 1. 检查 SSH 客户端 ---"

if command -v ssh &>/dev/null; then
    echo "SSH 客户端已安装：$(which ssh)"
    echo "版本信息："
    ssh -V 2>&1 | head -1
else
    echo "SSH 客户端未安装。"
    echo "安装方法（Debian/Ubuntu）：sudo apt install openssh-client"
    echo "安装方法（RHEL/CentOS）： sudo yum install openssh-clients"
fi

echo ""

# --------------------------------------------------
# 二、检查 SSH 服务端
# --------------------------------------------------
echo "--- 2. 检查 SSH 服务端（sshd）---"

# 检查 sshd 是否运行
if pgrep -x sshd &>/dev/null; then
    echo "SSH 服务端正在运行"
    echo "sshd 进程数：$(pgrep -x sshd | wc -l)"
    echo "sshd 监听端口："
    if command -v ss &>/dev/null; then
        ss -tlnp | grep sshd 2>/dev/null || echo "  （未找到监听端口，可能使用非标准端口）"
    elif command -v netstat &>/dev/null; then
        netstat -tlnp 2>/dev/null | grep sshd || echo "  （需要 root 权限查看 PID）"
    fi
elif [ -f /usr/sbin/sshd ] || [ -f /usr/bin/sshd ]; then
    echo "SSH 服务端已安装但未运行"
    echo "启动方法：sudo systemctl start sshd"
else
    echo "SSH 服务端未安装（本机可能只有客户端）"
    echo "安装方法（Debian/Ubuntu）：sudo apt install openssh-server"
    echo "安装方法（RHEL/CentOS）： sudo yum install openssh-server"
fi

echo ""
echo "==========================================="
echo "  加密概念类比说明"
echo "==========================================="
echo ""

# --------------------------------------------------
# 三、对称加密类比
# --------------------------------------------------
echo "--- 对称加密 ---"
cat << 'EOF'
类比：两人共用一把钥匙的保险箱。

  小明                          小红
   |                              |
   |--[用钥匙锁上箱子]------------->|
   |                              [用同一把钥匙开箱]
   |<------------[用钥匙锁上箱子]--|
   |                              |
  （钥匙通过安全方式事先交换）

特点：
  - 加密和解密用同一把钥匙
  - 速度快，适合大量数据
  - 但问题是：怎么安全地交换这把钥匙？

实际应用：AES-256 加密算法
EOF

echo ""
echo "--- 非对称加密 ---"
cat << 'EOF'
类比：公开的邮箱 + 私人钥匙。

  小明                          小红
   |                              |
   |  小红先公开一个"投递口"        |
   |  （公钥 — 任何人都能投信）     |
   |                              |
   |--[用投递口把信塞进去]--------->|
   |                              [用小红的私人钥匙打开]
   |                              （私钥 — 只有小红有）
   |                              |
   |<------------[用小明投递口]----|
   |                              |

特点：
  - 公钥加密，私钥解密
  - 公钥可以公开，不怕被拦截
  - 速度较慢，通常只用来交换"对称密钥"

实际应用：RSA、Ed25519
EOF

echo ""
echo "--- SSH 的混合加密流程 ---"
cat << 'EOF'
SSH 不只用一种加密，而是"混合使用"：

第1步：非对称加密
  客户端用服务器的公钥加密一个随机数
  → 只有拥有私钥的服务器能解密
  → 这个随机数成为"会话密钥"

第2步：对称加密
  之后的通信都用这个"会话密钥"进行对称加密
  → 因为对称加密速度快，适合传输大量数据

类比：
  先用昂贵的联邦快递（非对称加密）送一把钥匙
  然后用这把钥匙打开日常用的便宜快递柜（对称加密）
  以后都用便宜快递柜通信
EOF

echo ""
echo "==========================================="
echo "  SSH vs Telnet 对比"
echo "==========================================="
echo ""

cat << 'EOF'
类比：SSH 是加密对讲机，Telnet 是普通扩音器。

Telnet（不推荐使用）：
  - 所有内容明文传输（包括密码！）
  - 网络上的任何人都能"偷听"
  - 就像在大街上喊你的银行卡密码

SSH：
  - 所有通信加密，防止窃听
  - 支持公钥认证（不用每次输密码）
  - 可以建立加密隧道（端口转发）
  - 文件传输也有安全封装（SCP/SFTP）

对比表格：
+------------------+-------------------+-------------------+
| 特性             | SSH               | Telnet            |
+------------------+-------------------+-------------------+
| 数据传输加密     | 是（端到端）      | 否（明文）        |
| 身份认证         | 密码/密钥/证书   | 仅密码（明文）    |
| 默认端口         | 22                | 23                |
| 中间人攻击防护   | 有（指纹验证）    | 无                |
| 文件传输         | SCP/SFTP内置      | 需要额外工具      |
| 隧道/端口转发    | 支持              | 不支持            |
| 适用场景         | 所有远程管理      | 历史遗留/调试    |
+------------------+-------------------+-------------------+

EOF

echo ""
echo "==========================================="
echo "  使用建议"
echo "==========================================="
echo ""
echo "  1. 永远不要在生产环境中使用 Telnet"
echo "  2. 首次 SSH 连接时务必核对服务器指纹"
echo "  3. 优先使用密钥认证而不是密码认证"
echo "  4. 定期更新 OpenSSH 版本以获得安全补丁"
echo "  5. 考虑修改默认端口（22）以减少自动化攻击"
echo ""
echo "下一节我们将学习 SSH 的基本使用命令。"
