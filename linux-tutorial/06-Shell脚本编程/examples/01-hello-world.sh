#!/bin/bash
# ========================================
# 01-hello-world.sh — 你的第一个Shell脚本
# ========================================
# 功能：演示 shebang、echo、注释和脚本基本结构
# 用法：chmod +x 01-hello-world.sh && ./01-hello-world.sh
# ========================================

# 第一行 #!/bin/bash 叫做 "shebang"（井号+感叹号），
# 它告诉系统用哪个解释器来执行这个脚本。
# 如果写 #!/usr/bin/python3，那就是 Python 脚本。

# 井号开头的是注释，注释不会被当作命令执行。
# 写注释是好习惯，方便以后回头看懂自己的代码。

# --------------------------------------------------
# echo 命令：在屏幕上打印文字
# --------------------------------------------------
echo "你好，Linux 世界！"
echo "这是我的第一个 Shell 脚本。"

# echo 默认会在末尾加上换行。加 -n 可以不换行：
echo -n "这一行不会换行，"
echo "所以会接在后面显示。"

# echo -e 可以识别转义字符（如 \t 制表符，\n 换行）：
echo -e "第一列\t第二列\t第三列"

# --------------------------------------------------
# 输出带变量的问候语
# --------------------------------------------------
USER_NAME="小明"
TODAY=$(date +%Y-%m-%d)
echo "当前用户：$USER_NAME"
echo "今天是：$TODAY"

# --------------------------------------------------
# 展示 date 命令在脚本中的使用
# --------------------------------------------------
echo "-----------------------------"
echo "系统信息："
echo "主机名：$(hostname)"
echo "当前目录：$(pwd)"
echo "脚本路径：$0"
echo "-----------------------------"

# 脚本执行完毕后，可以使用 echo $? 查看退出码
# 0 表示成功，非 0 表示出错
exit 0
