#!/bin/bash
# ========================================
# 02-variables.sh — Shell 变量与环境变量
# ========================================
# 功能：演示变量的定义、引用、只读、删除，
#        以及环境变量和特殊变量的使用
# 用法：./02-variables.sh 参数1 参数2 参数3
# ========================================

echo "========================================="
echo "  一、自定义变量的定义和引用"
echo "========================================="

# 定义变量：变量名=值（等号两边不能有空格！）
name="小明"
age=25
score=98.5

# 引用变量：$变量名 或 ${变量名}
echo "名字：$name"
echo "年龄：${age}"        # ${} 形式更安全，尤其在拼接时
echo "成绩：$score"

# ${} 在拼接时的必要性
echo "小明今年${age}岁了"   # 正确
echo "小明今年$age岁了"     # 也正确（变量名后跟中文，bash 能识别边界）
# 但如果变量名后跟英文或数字，就需要 {}
file_prefix="backup"
echo "${file_prefix}_2026.tar.gz"  # 正确：backup_2026.tar.gz
# echo "$file_prefix_2026.tar.gz"   # 错误：bash 会找变量 file_prefix_2026

echo ""
echo "========================================="
echo "  二、只读变量（readonly）"
echo "========================================="

# readonly 将一个变量设为只读，不能再修改
readonly PI=3.14159
echo "圆周率：$PI"
# PI=3.14  # 这行如果取消注释，脚本会报错退出

# 也可以用 declare -r 声明只读变量
declare -r MAX_SIZE=1024
echo "最大容量：${MAX_SIZE}MB"

echo ""
echo "========================================="
echo "  三、删除变量（unset）"
echo "========================================="

# unset 可以删除一个变量（但不能删除只读变量）
temp_var="临时数据"
echo "删除前：$temp_var"
unset temp_var
echo "删除后：${temp_var:-<变量已不存在>}"  # 使用默认值技巧

echo ""
echo "========================================="
echo "  四、常用环境变量"
echo "========================================="

# 环境变量是系统预设的全局变量
echo "当前用户：$USER"
echo "家目录：$HOME"
echo "当前工作目录：$PWD"
echo "命令搜索路径(PATH)：$PATH"
echo "Shell类型：$SHELL"
echo "主机名：$HOSTNAME"
echo "当前语言：$LANG"
echo "登录名：$LOGNAME"

# 查看所有环境变量的命令（这里只显示前5行）
echo ""
echo "env 命令的前 5 行输出："
env | head -5

echo ""
echo "========================================="
echo "  五、export 导出变量"
echo "========================================="

# 普通变量只在当前脚本中有效
# export 可以让变量被子进程继承
greeting="你好"
export greeting          # 将这个变量告诉所有"子进程"
echo "已导出变量 greeting=$greeting"

# 可以一行完成定义和导出
export APP_NAME="MyBackupTool"
echo "已导出变量 APP_NAME=$APP_NAME"

echo ""
echo "========================================="
echo "  六、特殊变量（脚本参数相关）"
echo "========================================="

# $0: 脚本本身的名称
# $1, $2, ...: 第1个参数，第2个参数...
# $#: 参数的总个数
# $@: 所有参数（每个参数作为独立的字符串）
# $*: 所有参数（所有参数合并为一个字符串）
# $?: 上一条命令的退出码（0=成功，非0=失败）
# $$: 当前脚本的进程 ID（PID）

echo "脚本名：$0"
echo "第1个参数：${1:-<未传递>}"
echo "第2个参数：${2:-<未传递>}"
echo "第3个参数：${3:-<未传递>}"
echo "参数总数：$#"

# 遍历所有参数
echo ""
echo "遍历所有参数（使用 \$@）："
count=1
for arg in "$@"; do
    echo "  参数 $count = $arg"
    ((count++))
done

# 当前脚本的 PID
echo ""
echo "当前脚本 PID：$$"

# 用一个成功和失败的例子演示 $?
echo ""
echo "演示退出码 \$?："
ls /tmp > /dev/null 2>&1
echo "执行 'ls /tmp' 的退出码：$?（0=成功）"

ls /nonexistent_path 2>/dev/null
echo "执行 'ls /不存在的目录' 的退出码：$?（2=失败）"

echo ""
echo "========================================="
echo "  变量知识点总结"
echo "========================================="
echo "1. 变量定义：变量名=值（等号两边不能有空格）"
echo "2. 引用变量：\$变量名 或 \${变量名}"
echo "3. 只读变量：readonly 或 declare -r"
echo "4. 删除变量：unset（不能删除只读变量）"
echo "5. 环境变量：系统预设，如 \$HOME \$PATH \$USER"
echo "6. export：将变量传递给子进程"
echo "7. 特殊变量：\$0(脚本名) \$1(参数) \$#(参数个数) \$?(退出码) \$\$(PID)"

exit 0
