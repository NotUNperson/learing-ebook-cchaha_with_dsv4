#!/bin/bash
# ============================================================================
# 01-directory-tree.sh
# 演示 Linux 目录树结构的关键目录及其用途
# 这个脚本会探索并展示 Linux 文件系统的核心目录结构
# ============================================================================

echo "=============================================="
echo "  Linux 目录树探索脚本"
echo "  本脚本将带你"参观"Linux 文件系统的各个关键目录"
echo "  就像拿着小区地图在小区里转一圈"
echo "=============================================="
echo ""

# ============================================================================
# 第一步：来到根目录，查看顶层目录结构
# ============================================================================
echo "【第一步】来到小区大门口（根目录 /）"
echo "命令: cd /"
echo ""
cd /

echo "查看根目录下有哪些'区域'（一级子目录）:"
echo "命令: ls -1 /"
echo "---"
ls -1 /
echo ""

# ============================================================================
# 第二步：展示每个关键目录的用途（用 echo 说明 + ls 展示）
# ============================================================================

# /home -- 居民楼，每个用户的家
echo "=============================================="
echo "【/home】居民楼 -- 每个用户的家目录"
echo "类似于小区里的住宅楼，每家每户有自己的房间"
echo "---"
echo "查看 /home 下有哪些住户:"
ls -l /home/ 2>/dev/null || echo "  (目前没有其他用户)"
echo ""
echo "查看当前用户的家目录（~ 就是你的家）:"
echo "命令: ls -la ~"
ls -la ~ 2>/dev/null
echo ""

# /etc -- 物业管理中心，配置文件
echo "=============================================="
echo "【/etc】物业管理中心 -- 系统和软件的配置文件"
echo "所有的'规章制度'（配置）都写在这里的纯文本文件里"
echo "---"
echo "查看 /etc 的部分内容（只显示前 20 个）:"
ls -1 /etc | head -20
echo "  ... (还有很多)"
echo ""
echo "几个重要的配置文件举例:"
echo "  /etc/passwd    -- 住户登记表（用户账户信息）"
echo "  /etc/fstab     -- 储物间分配表（文件系统挂载配置）"
echo "  /etc/hostname  -- 小区名称（计算机名）"
echo "  /etc/os-release -- 小区物业公司信息（系统版本）"
echo ""

# 读取并展示几个示例配置文件的内容（如果存在）
echo "【示例】查看 /etc/os-release（系统版本信息）:"
if [ -f /etc/os-release ]; then
    cat /etc/os-release
else
    echo "  (此文件在当前系统中不存在)"
fi
echo ""

# /var -- 监控中心与仓库
echo "=============================================="
echo "【/var】监控中心+仓库 -- 经常变化的数据"
echo "日志、缓存、打印队列等'动态数据'都放在这里"
echo "---"
echo "查看 /var 的子目录:"
ls -1 /var 2>/dev/null
echo ""
echo "  /var/log   -- 监控录像（系统日志）"
echo "  /var/cache -- 临时存货区（缓存）"
echo "  /var/spool -- 待处理队列（打印、邮件）"
echo ""
echo "【示例】查看 /var/log 下的日志文件（部分）:"
ls -lh /var/log/ 2>/dev/null | head -10
echo ""

# /usr -- 商业街与公共设施
echo "=============================================="
echo "【/usr】商业街+公共设施 -- 用户程序和共享资源"
echo "绝大部分你安装的软件都放在这里"
echo "---"
echo "查看 /usr 的子目录:"
ls -1 /usr 2>/dev/null
echo ""
echo "  /usr/bin     -- 商铺（可执行程序）"
echo "  /usr/lib     -- 仓库（库文件）"
echo "  /usr/share   -- 公告栏（文档、图标等共享数据）"
echo "  /usr/local   -- 自营店（手动编译安装的软件）"
echo ""

# /tmp -- 临时垃圾堆放点
echo "=============================================="
echo "【/tmp】临时堆放点 -- 所有人都能用的临时文件区"
echo "重启后可能被清空，不要放重要文件！"
echo "---"
echo "查看 /tmp 下当前有什么:"
ls -la /tmp/ 2>/dev/null | head -10
echo ""

# /dev -- 设备档案柜
echo "=============================================="
echo "【/dev】设备档案柜 -- 每个硬件都表现为一个文件"
echo "硬盘是文件！键盘是文件！甚至黑洞也是文件！"
echo "---"
echo "查看 /dev 中的部分设备文件（前 15 个）:"
ls -1 /dev | head -15
echo "  ... (还有很多设备文件)"
echo ""
echo "几个有趣的设备文件:"
echo "  /dev/sda    -- 第一块硬盘"
echo "  /dev/null   -- '黑洞'，扔进去的东西会永久消失"
echo "  /dev/zero   -- 无限产生零的'泉水'"
echo "  /dev/random -- 随机数生成器"
echo ""

# /proc -- 实时监控仪表盘（虚拟文件系统）
echo "=============================================="
echo "【/proc】实时监控仪表盘 -- 虚拟文件系统（在内存里）"
echo "系统和进程的实时状态，不是真实文件"
echo "---"
echo "查看 /proc 下的系统信息文件:"
ls -1 /proc | head -15
echo "  ... (还有很多)"
echo ""
echo "几个有用的 /proc 文件:"
if [ -f /proc/cpuinfo ]; then
    echo "  【CPU 信息】:"
    grep "model name" /proc/cpuinfo | head -1
fi
if [ -f /proc/meminfo ]; then
    echo "  【内存信息】:"
    grep "MemTotal" /proc/meminfo
fi
echo ""

# /boot -- 发电站，启动文件
echo "=============================================="
echo "【/boot】发电站 -- 系统启动所必需的核心文件"
echo "内核镜像和引导加载程序的配置文件"
echo "---"
echo "查看 /boot 的内容:"
ls -lh /boot/ 2>/dev/null | head -10
echo ""

# ============================================================================
# 第三步：展示目录树的层次关系（用 tree 或 find 模拟）
# ============================================================================
echo "=============================================="
echo "【第三步】目录树层次关系概览"
echo "用 find 命令展示根目录下两层的目录结构:"
echo ""
find / -maxdepth 2 -type d 2>/dev/null | sort | head -30
echo "  ... (还有更多)"
echo ""

# ============================================================================
# 第四步：磁盘空间查看 -- 看各分区是如何'挂'在目录树上的
# ============================================================================
echo "=============================================="
echo "【第四步】磁盘使用情况 -- 看分区怎么'挂'在树上"
echo "df -h 命令告诉你每个分区的挂载点"
echo ""
df -h 2>/dev/null
echo ""

# ============================================================================
# 总结
# ============================================================================
echo "=============================================="
echo "  参观完毕！总结一下:"
echo ""
echo "  /     = 小区大门口（一切的起点）"
echo "  /home = 居民楼（你的文件放这里）"
echo "  /etc  = 物业管理中心（配置文件）"
echo "  /var  = 监控中心+仓库（日志和缓存）"
echo "  /usr  = 商业街（安装的软件）"
echo "  /tmp  = 临时堆放点（可能被清理）"
echo "  /dev  = 设备档案柜（硬件即文件）"
echo "  /proc = 实时仪表盘（虚拟文件系统）"
echo "  /boot = 发电站（启动文件）"
echo ""
echo "  记住：在 Linux 里，一切皆文件，一切从 / 开始！"
echo "=============================================="
