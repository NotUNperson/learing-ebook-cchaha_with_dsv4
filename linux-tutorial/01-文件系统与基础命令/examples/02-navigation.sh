#!/bin/bash
# ============================================================================
# 02-navigation.sh
# 演示 pwd、cd、ls 命令的常用操作和选项
# 学会在 Linux 目录树中自由导航和查看内容
# ============================================================================

echo "=============================================="
echo "  Linux 导航与查看命令演示"
echo "  pwd -- 我在哪里"
echo "  cd  -- 怎么走"
echo "  ls  -- 这里有什么"
echo "=============================================="
echo ""

# ============================================================================
# 第一部分：pwd -- 查看当前位置
# ============================================================================
echo "【第一部分】pwd -- 查看当前工作目录"
echo "就像 GPS 定位，告诉你现在在哪"
echo ""

echo "当前目录是:"
pwd
echo ""

# ============================================================================
# 第二部分：cd -- 在目录树中移动
# ============================================================================
echo "=============================================="
echo "【第二部分】cd -- 在目录树中穿梭"
echo ""

# 记录起始目录，演示完后方便回去
START_DIR=$(pwd)
echo "起始目录: $START_DIR"
echo ""

# cd / -- 去根目录
echo "1. cd / -- 去小区大门口（根目录）"
cd /
echo "   当前位置: $(pwd)"
echo ""

# cd ~ -- 回家
echo "2. cd ~ -- 无论在哪，一键回家"
cd ~
echo "   当前位置: $(pwd)"
echo ""

# cd .. -- 返回上一级
echo "3. cd .. -- 返回上一级目录"
cd ..
echo "   当前位置: $(pwd)"
echo ""

# cd ../.. -- 返回上两级
echo "4. cd ../.. -- 返回上两级目录"
cd ../..
echo "   当前位置: $(pwd)"
echo ""

# cd - -- 返回上一个目录
echo "5. cd - -- 回到刚才所在的地方（'返回键'）"
cd -
echo "   现在又在: $(pwd)"
echo ""

# cd - 再按一次，在两个目录间跳转
echo "6. 再按一次 cd -，在两个目录间来回跳转:"
cd -
echo "   又回到了: $(pwd)"
echo ""

# 演示绝对路径 vs 相对路径
echo "--- 绝对路径 vs 相对路径 ---"
echo "绝对路径（从 / 出发）: cd /home"
cd /home
echo "   当前位置: $(pwd)"
echo "相对路径（从当前位置出发）: 如果现在在 /home，cd .. 会去 /"
cd ..
echo "   cd .. 后: $(pwd)"
echo ""

# 回到起始目录
cd "$START_DIR"
echo "回到起始目录: $(pwd)"
echo ""

# ============================================================================
# 第三部分：ls -- 查看目录内容
# ============================================================================
echo "=============================================="
echo "【第三部分】ls -- 查看目录里有什么"
echo ""

# 先创建一个演示用的目录结构
DEMO_DIR="/tmp/nav-demo-$$"
mkdir -p "$DEMO_DIR/subdir"
touch "$DEMO_DIR/file1.txt"
touch "$DEMO_DIR/file2.txt"
touch "$DEMO_DIR/.hidden_file"
touch "$DEMO_DIR/subdir/nested_file.txt"
cd "$DEMO_DIR"

echo "演示目录: $DEMO_DIR"
echo "已准备好以下演示文件:"
echo "  file1.txt        -- 普通文件"
echo "  file2.txt        -- 普通文件"
echo "  .hidden_file     -- 隐藏文件（以 . 开头）"
echo "  subdir/          -- 子目录"
echo "    nested_file.txt -- 子目录中的文件"
echo ""

# ls -- 基本列表
echo "1. ls -- 基本列表（不显示隐藏文件）:"
ls
echo "   注意：.hidden_file 没有显示出来"
echo ""

# ls -a -- 显示所有文件包括隐藏
echo "2. ls -a -- 显示包括隐藏文件在内的所有文件:"
ls -a
echo "   注意：.hidden_file 出现了，还有 . 和 .."
echo ""

# ls -A -- 显示几乎所有文件，但不显示 . 和 ..
echo "3. ls -A -- 显示隐藏文件，但不显示 . 和 ..:"
ls -A
echo "   更干净，只有真正的文件"
echo ""

# ls -l -- 长格式
echo "4. ls -l -- 长格式，显示详细信息:"
ls -l
echo ""
echo "   每一列的含义:"
echo "   -rw-r--r-- 1 user group size date name"
echo "   权限      链接数 所有者 组   大小  日期  文件名"
echo ""

# ls -lh -- 人类可读的文件大小
echo "5. ls -lh -- 人类可读的文件大小（K/M/G）:"
ls -lh
echo "   文件大小用 K/M/G 显示，比纯数字好懂"
echo ""

# ls -lt -- 按时间排序
echo "6. ls -lt -- 按修改时间排序（新的在前）:"
# 修改一下文件的时间来演示
sleep 1
touch file1.txt
ls -lt
echo "   file1.txt 刚被 touch 过，所以排在最前面"
echo ""

# ls -ltr -- 按时间排序并反转
echo "7. ls -ltr -- 按修改时间排序，最旧的在前:"
ls -ltr
echo "   最旧的文件排在最上面"
echo ""

# ls -R -- 递归列出
echo "8. ls -R -- 递归列出所有子目录:"
ls -R
echo "   不仅有当前目录的内容，还有 subdir/ 里面的 nested_file.txt"
echo ""

# ls -lah -- 最常用组合
echo "9. ls -lah -- 最常用组合（长格式 + 所有文件 + 人类可读）:"
ls -lah
echo "   这是 Linux 用户的肌肉记忆命令"
echo ""

# ls -lhS -- 按文件大小排序
echo "10. ls -lhS -- 按文件大小排序（大的在前）:"
ls -lhS
echo ""

# ls -d */ -- 只显示目录不显示文件
echo "11. ls -d */ -- 只列出子目录，不列出文件:"
ls -d */
echo ""

# ============================================================================
# 第四部分：实战组合
# ============================================================================
echo "=============================================="
echo "【第四部分】实战场景演示"
echo ""

# 场景1：查看 /etc 下最近被修改的配置文件
echo "场景1: 查看 /etc 下最近修改的文件（最近 5 个）"
echo "命令: ls -lt /etc | head -6"
echo "---"
ls -lt /etc 2>/dev/null | head -6
echo ""

# 场景2：查看家目录的磁盘占用（带颜色更友好）
echo "场景2: 查看家目录的全部内容（包括隐藏文件）"
echo "命令: ls -lah ~"
echo "---"
ls -lah ~ 2>/dev/null | head -20
echo "  ... (可能还有更多)"
echo ""

# ============================================================================
# 清理演示文件
# ============================================================================
echo "=============================================="
echo "清理演示文件..."
rm -rf "$DEMO_DIR"
echo "已删除: $DEMO_DIR"
echo ""

# ============================================================================
# 总结
# ============================================================================
echo "=============================================="
echo "  总结:"
echo ""
echo "  pwd          -- 显示当前目录（GPS 定位）"
echo "  cd <目录>    -- 切换到指定目录"
echo "  cd ~         -- 回家"
echo "  cd -         -- 返回上一个目录（'返回键'）"
echo "  cd ..        -- 返回上一级"
echo ""
echo "  ls           -- 列出目录内容"
echo "  ls -l        -- 长格式（详细信息）"
echo "  ls -a        -- 包括隐藏文件"
echo "  ls -h        -- 人类可读的文件大小"
echo "  ls -t        -- 按时间排序"
echo "  ls -r        -- 反转排序"
echo "  ls -R        -- 递归列出子目录"
echo "  ls -lah      -- 最常用组合"
echo ""
echo "  记住：绝对路径从 / 出发，相对路径从当前目录出发"
echo "=============================================="
