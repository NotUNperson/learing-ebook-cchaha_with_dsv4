#!/bin/bash
# =============================================================================
# 01-daily-workflow.sh — 日常开发工作流实操脚本
# 场景：模拟开发者一天的完整工作循环
#      早上 pull → 建分支 → 写代码 → add → commit → push → 提 PR
# 用法：在 Git Bash 中运行  bash 01-daily-workflow.sh
# 注意：该脚本在 /tmp 下创建临时目录，运行结束后自动清理，不污染你的文件
# =============================================================================

echo "============================================"
echo "  01 日常开发工作流 — 实操脚本"
echo "============================================"
echo ""

# ---------------------------------------------------------------------------
# 准备实验场地：创建一个临时项目，模拟"远程仓库"和"本地仓库"
# ---------------------------------------------------------------------------

# 创建两个临时目录：一个模拟远程仓库（如 GitHub），一个模拟你的本地仓库
REMOTE_DIR="/tmp/git-learn-remote-$(date +%s)"
LOCAL_DIR="/tmp/git-learn-local-$(date +%s)"

echo ">>> 模拟场景说明："
echo "    你有两个目录："
echo "    - remote/  扮演 GitHub 上的远程仓库（bare repo）"
echo "    - local/   扮演你自己电脑上的本地仓库"
echo ""

# 创建"远程"仓库（bare 仓库，模拟 GitHub 上的仓库）
mkdir -p "$REMOTE_DIR"
cd "$REMOTE_DIR" || exit 1
git init --bare   # --bare 创建一个"裸"仓库，专门用来当远程仓库，没有工作目录
echo ">>> 远程仓库已创建（bare repo，模拟 GitHub 上的仓库）"
echo ""

# 把"远程"仓库克隆到本地（模拟 git clone 从 GitHub 拉代码）
cd /tmp || exit
git clone "$REMOTE_DIR" "$LOCAL_DIR"
cd "$LOCAL_DIR" || exit 1
echo ">>> 本地仓库已从远程仓库克隆下来"
echo ""

# 在 main 上做一个初始提交（远程仓库需要一个初始提交，否则无法 push）
echo ">>> 在 main 上创建项目的初始文件"
echo "# 我的项目" > README.md
git add README.md
git commit -m "初始化项目，添加 README"
git push -u origin main
echo ""
echo ">>> 初始提交已推送到远程仓库"
echo ""

# ============================================================================
# 第一部分：早上来到工位 —— 拉取最新代码
# ============================================================================
echo "============================================"
echo "  第一步：早上来到工位 — git pull"
echo "============================================"
echo ""
echo "每天早上开始写代码前，第一件事是拉取远程最新代码。"
echo "因为昨晚同事可能已经推送了新内容。"
echo ""

# 确认当前在 main 分支
echo ">>> 当前所在分支："
git branch
echo ""

# 拉取远程最新代码
echo ">>> 执行 git pull 拉取最新代码："
git pull origin main
echo ""
echo "（因为我们刚 push 完，所以显示 Already up to date，没有新内容）"
echo ""

# ============================================================================
# 第二部分：创建功能分支
# ============================================================================
echo "============================================"
echo "  第二步：领任务 — 创建功能分支"
echo "============================================"
echo ""
echo "假设今天的任务是：为网站添加一个"关于我们"页面。"
echo "我们创建一个新分支来开发这个功能："
echo ""

echo ">>> 创建并切换到功能分支："
git checkout -b feature/add-about-page
echo ""
echo ">>> 当前所在分支："
git branch
echo "（注意 * 号在 feature/add-about-page 前面，表示我们在这个分支上）"
echo ""

# ============================================================================
# 第三部分：写代码（模拟 — 创建文件）
# ============================================================================
echo "============================================"
echo "  第三步：写代码"
echo "============================================"
echo ""
echo "现在我们在功能分支上开始写代码（这里用 echo 生成文件来模拟）："
echo ""

# 模拟编写 HTML 文件
echo ">>> 创建 about.html（模拟写代码的过程）"
cat > about.html << 'HTML_EOF'
<!DOCTYPE html>
<html lang="zh-CN">
<head>
    <meta charset="UTF-8">
    <title>关于我们</title>
</head>
<body>
    <h1>关于我们</h1>
    <p>我们是一个优秀的团队。</p>
</body>
</html>
HTML_EOF
echo ">>> about.html 文件已创建"
echo ""

echo ">>> 当前文件列表："
ls -la
echo ""

# ============================================================================
# 第四步：查看状态 + 暂存 + 提交
# ============================================================================
echo "============================================"
echo "  第四步：add → commit（拍照存档）"
echo "============================================"
echo ""

# 查看当前状态
echo ">>> 查看当前工作区状态："
git status
echo ""

# 暂存文件（git add）
echo ">>> 暂存 about.html（git add）："
git add about.html
echo ""

echo ">>> 再次查看状态（文件已进入暂存区，显示为绿色）："
git status
echo ""

# 提交（git commit）
echo ">>> 提交改动（git commit）："
git commit -m "feat: 添加"关于我们"页面，包含标题和团队介绍"
echo ""

echo ">>> 再次查看状态（工作区干净了）："
git status
echo ""

# ============================================================================
# 第五步：推送到远程仓库
# ============================================================================
echo "============================================"
echo "  第五步：git push（推送到远程仓库）"
echo "============================================"
echo ""
echo "commit 只是保存在你本地电脑上，同事还看不见。"
echo "需要 push 把代码上传到远程仓库。"
echo ""

echo ">>> 推送功能分支到远程仓库："
git push -u origin feature/add-about-page
echo ""
echo "（-u 参数把本地分支和远程同名分支绑定，下次只需 git push 即可）"
echo ""

# ============================================================================
# 第六步：模拟同事 review 后的修改
# ============================================================================
echo "============================================"
echo "  第六步（附加）：模拟同事 review 后的修改"
echo "============================================"
echo ""
echo "在实际团队中，同事 review 你的 PR 后可能会提意见。"
echo "假设同事看完说："团队介绍可以再详细一点"，我们修改后再次提交："
echo ""

# 修改文件
echo ">>> 按照同事意见修改 about.html..."
sed -i 's/我们是一个优秀的团队。/我们是一个由设计师、工程师和产品经理组成的优秀团队。/' about.html
echo ""

echo ">>> 查看改动内容："
git diff
echo ""

# 再次 add + commit
echo ">>> 暂存修改并提交："
git add about.html
git commit -m "fix: 根据 review 意见，细化团队介绍内容"
echo ""

echo ">>> 推送修改："
git push
echo "（这次不需要 -u 了，因为已经绑定过）"
echo ""

# ============================================================================
# 第七步：合并到 main（模拟 PR 合并）
# ============================================================================
echo "============================================"
echo "  第七步：合并到 main（模拟 PR merge）"
echo "============================================"
echo ""
echo "在真实工作流中，合并是在 GitHub 上点 Merge 按钮完成的。"
echo "这里我们用命令行模拟合并过程："
echo ""

# 切回 main 并合并
echo ">>> 切回 main 分支："
git checkout main
echo ""

echo ">>> 合并功能分支："
git merge feature/add-about-page
echo ""

echo ">>> 推送更新后的 main 到远程："
git push origin main
echo ""

# ============================================================================
# 第八步：查看完整提交历史
# ============================================================================
echo "============================================"
echo "  最后：查看今天的完整提交历史"
echo "============================================"
echo ""

git log --oneline --all --graph
echo ""

echo "============================================"
echo "  总结：一个完整的工作日流程"
echo "============================================"
echo ""
echo "  1. git checkout main           # 切到主分支"
echo "  2. git pull                    # 拉取最新代码"
echo "  3. git checkout -b feature/xxx # 创建功能分支"
echo "  4. （写代码、改代码）           # 开发过程"
echo "  5. git add .                   # 暂存改动"
echo "  6. git commit -m \"...\"         # 提交改动"
echo "  7. git push -u origin feature/xxx # 推送到远程"
echo "  8. 在 GitHub 上创建 PR         # 请同事审查"
echo "  9. （根据意见修改，重复 4-7）   # 修改循环"
echo " 10. 在 GitHub 上 Merge          # 合并到 main"
echo ""

# ---------------------------------------------------------------------------
# 清理：删除临时目录
# ---------------------------------------------------------------------------
echo "============================================"
echo "  清理临时目录..."
echo "============================================"
cd /tmp || exit
rm -rf "$REMOTE_DIR" "$LOCAL_DIR"
echo ">>> 已删除：$REMOTE_DIR"
echo ">>> 已删除：$LOCAL_DIR"
echo ""
echo "提示：你电脑上的真实文件完全没有被影响。"
