#!/bin/bash
# =============================================================================
# 08-capstone.sh — 综合练习：用 Git 管理个人笔记项目
# 场景：模拟一个完整的学习笔记管理流程，串联所有知识点
# 用法：在 Git Bash 中运行  bash 08-capstone.sh
# 该脚本在 /tmp 下创建临时目录，运行结束后自动清理
# =============================================================================

echo "============================================"
echo "  08 综合练习：用 Git 管理个人笔记 — 实操脚本"
echo "============================================"
echo ""
echo "  本脚本会自动模拟一个完整的学习笔记管理流程："
echo "  init → add → commit → log → diff → restore → reset → revert"
echo "  请观察每一步的输出，理解整个工作流。"
echo ""

# 1. 创建临时实验场地
TEMP_DIR="/tmp/git-learn-08-capstone-$(date +%s)"
mkdir -p "$TEMP_DIR"
cd "$TEMP_DIR" || exit 1
echo ">>> 实验场地（模拟你的个人笔记项目目录）：$TEMP_DIR"
echo ""

# ===========================================================================
# 阶段一：初始化项目
# ===========================================================================
echo "============================================"
echo "  阶段一：初始化项目（git init）"
echo "============================================"
echo ""

git init
echo ""
echo ">>> 仓库已初始化。这就是你管理个人笔记的'档案柜'。"
echo ""

# ===========================================================================
# 阶段二：第一次提交 —— 创建第一篇笔记
# ===========================================================================
echo "============================================"
echo "  阶段二：第一次提交 —— 创建第一篇学习笔记"
echo "============================================"
echo ""

# 创建第一篇笔记
cat > "2026-04-27-git-basics.md" << 'EOF'
# Git 基础学习笔记

## git init
- 把普通文件夹变成 Git 仓库
- 会在文件夹中创建 .git 隐藏目录
- .git 目录存放所有版本数据

## 三个区域
- 工作区 = 办公桌（写代码的地方）
- 暂存区 = 文件夹（准备存档的地方）
- 仓库 = 档案柜（永久保存的地方）
EOF

echo ">>> 创建了第一篇笔记：2026-04-27-git-basics.md"
echo ""

echo ">>> git status（新文件在办公桌上，还没跟踪）："
git status
echo ""

echo ">>> 执行 git add . 把笔记放入暂存区："
git add .
echo ""

echo ">>> 执行 git commit 正式存档："
git commit -m "docs: 添加 Git 基础学习笔记"
echo ""

# ===========================================================================
# 阶段三：日常更新 —— 连续几天添加笔记
# ===========================================================================
echo "============================================"
echo "  阶段三：日常更新 —— 连续补充笔记"
echo "============================================"
echo ""

echo "第二天：学习了 git add，补充笔记内容"

echo "" >> "2026-04-27-git-basics.md"
echo "## git add" >> "2026-04-27-git-basics.md"
echo "- 把工作区的修改放入暂存区" >> "2026-04-27-git-basics.md"
echo "- 支持选择性暂存（git add -p）" >> "2026-04-27-git-basics.md"

git add "2026-04-27-git-basics.md"
git commit -m "docs: 补充 git add 相关内容"
echo ">>> 第二次提交完成"
echo ""

echo "第三天：学习了 git commit，继续补充"

echo "" >> "2026-04-27-git-basics.md"
echo "## git commit" >> "2026-04-27-git-basics.md"
echo "- 把暂存区内容永久存入仓库" >> "2026-04-27-git-basics.md"
echo "- 提交信息要清晰描述'做了什么'" >> "2026-04-27-git-basics.md"

git add "2026-04-27-git-basics.md"
git commit -m "docs: 补充 git commit 相关内容"
echo ">>> 第三次提交完成"
echo ""

echo "第四天：学习了终端基础，创建第二篇笔记"

cat > "2026-04-28-terminal-basics.md" << 'EOF'
# 终端基础学习笔记

## 常用命令
- ls：列出当前目录的文件
- cd：切换目录
- pwd：显示当前路径
- mkdir：创建目录
EOF

git add .
git commit -m "docs: 添加终端基础学习笔记"
echo ">>> 第四次提交完成"
echo ""

# ===========================================================================
# 阶段四：修正笔记中的错误
# ===========================================================================
echo "============================================"
echo "  阶段四：修正错误 —— 发现笔记中有术语错误"
echo "============================================"
echo ""

# 模拟：笔记中有一个错误（我们把它改"对"然后再"改错"来演示）
echo ">>> 在 git 基础笔记中修改一行内容（修正术语）："
# 先用 sed 模拟修改文件内容
sed -i 's/- 支持选择性暂存（git add -p）/- 支持选择性暂存（git add -p），可精细控制每一块修改/' "2026-04-27-git-basics.md"

git diff "2026-04-27-git-basics.md"
echo ""

git add "2026-04-27-git-basics.md"
git commit -m "fix: 补充 git add -p 的说明"
echo ">>> 第五次提交完成（修正了一个错误/遗漏）"
echo ""

# ===========================================================================
# 阶段五：查看学习历史
# ===========================================================================
echo "============================================"
echo "  阶段五：回顾学习历史（git log）"
echo "============================================"
echo ""

echo ">>> 你的学习足迹（git log --oneline）："
git log --oneline
echo ""

echo ">>> 详细的修改统计（git log --stat）："
git log --stat --max-count=3
echo ""

echo ">>> 只看 Git 基础笔记的修改历史："
git log --oneline -- "2026-04-27-git-basics.md"
echo ""

# ===========================================================================
# 阶段六：意外恢复 —— 用 git restore
# ===========================================================================
echo "============================================"
echo "  阶段六：意外恢复 —— 不小心删错内容"
echo "============================================"
echo ""

echo ">>> 模拟操作：不小心把笔记文件搞坏了"
echo "这行是意外添加的错误内容！！应该删除！！" >> "2026-04-27-git-basics.md"
echo ""

echo ">>> git status（发现文件被修改了）："
git status
echo ""

echo ">>> git diff（看看改了什么）："
git diff "2026-04-27-git-basics.md"
echo ""

echo ">>> 后悔了！用 git restore 恢复："
git restore "2026-04-27-git-basics.md"
echo ""

echo ">>> git status（工作区恢复干净）："
git status
echo ""

echo ">>> 验证笔记文件已恢复（最后几行不再有错误内容）："
tail -3 "2026-04-27-git-basics.md"
echo ""

# ===========================================================================
# 阶段七：修正提交信息 —— 用 git reset
# ===========================================================================
echo "============================================"
echo "  阶段七：修正提交信息 —— git reset --soft"
echo "============================================"
echo ""

echo ">>> 模拟：创建一次提交，但提交信息不好"
echo "学习总结：Git 真的很强大" > "learn-summary.md"
git add "learn-summary.md"
git commit -m "改了东西" 2>/dev/null
echo ""

echo ">>> git log --oneline（最后一条的提交信息很模糊）："
git log --oneline -3
echo ""

echo ">>> 后悔了！用 git reset --soft HEAD~1 撤销："
git reset --soft HEAD~1
echo ""

echo ">>> git status（修改回到暂存区）："
git status
echo ""

echo ">>> 用更好的提交信息重新提交："
git commit -m "docs: 添加 Git 学习总结笔记"
echo ""

echo ">>> 修正后的提交历史："
git log --oneline -3
echo ""

# ===========================================================================
# 阶段八：git revert —— 保留历史的撤销
# ===========================================================================
echo "============================================"
echo "  阶段八：git revert —— 保留历史的撤销"
echo "============================================"
echo ""

echo ">>> 模拟：添加了一个后来发现不该要的文件"
echo "这个文件不应该存在" > "unwanted.md"
git add "unwanted.md"
git commit -m "添加了不该添加的文件" 2>/dev/null

UNWANTED_COMMIT=$(git log --oneline -1 --format="%h")
echo ">>> 提交了 unwanted.md（哈希: $UNWANTED_COMMIT）"
echo ""

echo ">>> 执行 git revert（创建反向提交来抵消）："
git revert --no-edit "$UNWANTED_COMMIT"
echo ""

echo ">>> 提交历史（注意多了一条 Revert 提交）："
git log --oneline -3
echo ""

echo ">>> 文件列表（unwanted.md 已消失）："
ls *.md
echo ""

# ===========================================================================
# 最终总结
# ===========================================================================
echo "============================================"
echo "  全部流程回顾"
echo "============================================"
echo ""

echo ">>> 完整的提交历史（你的完整学习足迹）："
git log --oneline --graph
echo ""

echo ">>> 你的笔记项目目录："
ls -la
echo ""

echo "============================================"
echo "  恭喜！你完成了一个完整的 Git 本地管理流程。"
echo "============================================"
echo ""
echo "回顾整个流程中你用到的技能："
echo "  1. git init                  创建仓库"
echo "  2. git add / git commit      日常提交"
echo "  3. git log                   查看历史"
echo "  4. git status / git diff     查看状态和改动"
echo "  5. git restore               恢复意外修改"
echo "  6. git reset --soft          修正提交信息"
echo "  7. git revert                保留历史的撤销"
echo ""
echo "这些就是 Git 本地版本管理的全部核心技能。"
echo "你现在已经可以独立用 Git 管理自己的项目了。"
echo ""
echo "核心循环记住：修改 → 暂存 → 提交 → 查看 → （需要时）后悔"
echo ""

# 清理
echo "============================================"
echo "  脚本执行完毕，清理临时目录..."
echo "============================================"
cd /tmp || exit
rm -rf "$TEMP_DIR"
echo ">>> 已删除 $TEMP_DIR"
echo ""
echo "建议：现在打开 Git Bash，自己重新走一遍这个流程。"
echo "不要复制粘贴——自己敲每一条命令，感受文件在三个区域之间流转。"
echo "用自己的学习笔记作为内容，这样你会更投入，记得更牢。"
