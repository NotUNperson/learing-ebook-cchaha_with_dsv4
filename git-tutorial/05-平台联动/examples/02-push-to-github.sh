#!/bin/bash
# ============================================================
# 02-本地仓库推送到GitHub - 示例脚本
# 功能：在本地创建一个完整的练习流程
#       模拟 git init → add → commit → remote add → push 全过程
# 注意：push 操作使用了不存在的远程地址，因此 push 会失败，
#       但这不影响你理解整个流程。你需要把地址替换成自己的。
# ============================================================

set -e

echo "========================================"
echo "  本地仓库推送到 GitHub 演示脚本"
echo "========================================"
echo ""

# -----------------------------------------------------------
# 第一步：创建临时目录作为"本地项目"
# -----------------------------------------------------------
DEMO_DIR=$(mktemp -d /tmp/git-push-demo-XXXXXX)
cd "$DEMO_DIR"
echo "[INFO] 演示目录：$DEMO_DIR"
echo ""

# -----------------------------------------------------------
# 第二步：初始化本地 Git 仓库
# -----------------------------------------------------------
echo ">>> 第一步：git init —— 初始化本地仓库"
echo ""

git init

echo ""
echo "   git init 在当前目录创建了一个 .git 隐藏文件夹，"
echo "   这个文件夹是 Git 的"大脑"，记录所有的版本信息。"
echo ""

# -----------------------------------------------------------
# 第三步：创建一些项目文件
# -----------------------------------------------------------
echo ">>> 第二步：创建项目文件"
echo ""

echo "# 我的第一个项目" > README.md
echo "这是一个从本地推送到 GitHub 的练习项目。" >> README.md

mkdir src
echo "console.log('Hello, World!');" > src/index.js

echo "# 忽略 node_modules 目录" > .gitignore
echo "node_modules/" >> .gitignore

# 看看创建了什么
ls -la
echo ""
ls -la src/

echo ""
echo "   创建了三个文件："
echo "   - README.md：项目说明"
echo "   - src/index.js：源代码（假装是个项目）"
echo "   - .gitignore：告诉 Git 忽略哪些文件"
echo ""

# -----------------------------------------------------------
# 第四步：查看状态
# -----------------------------------------------------------
echo ">>> 第三步：git status —— 查看当前状态"
echo ""

git status

echo ""
echo "   三个文件都是红色的"未追踪"状态，Git 看到了它们但还没纳入管理。"
echo ""

# -----------------------------------------------------------
# 第五步：暂存并提交
# -----------------------------------------------------------
echo ">>> 第四步：git add —— 暂存所有文件"
echo ""

git add .

echo ""
echo ">>> 第五步：git commit —— 创建第一个提交"
echo ""

git commit -m "初始提交：项目骨架搭建"

echo ""
echo "   现在本地仓库有了第一个快照（版本）。"
echo ""

# -----------------------------------------------------------
# 第六步：查看提交历史
# -----------------------------------------------------------
echo ">>> 第六步：git log —— 确认提交"
echo ""

git log --oneline

echo ""

# -----------------------------------------------------------
# 第七步：演示 git remote add（使用一个占位地址）
# -----------------------------------------------------------
echo ">>> 第七步：git remote add origin —— 添加远程地址"
echo ""

# 使用占位地址，你需要替换成你自己的 GitHub 仓库 SSH 地址
PLACEHOLDER_URL="git@github.com:你的用户名/你的仓库名.git"

git remote add origin "$PLACEHOLDER_URL"

echo "   命令：git remote add origin $PLACEHOLDER_URL"
echo ""
echo "   origin = 这个远程地址的"小名"（约定俗成的默认名字）"
echo "   上面的地址是占位的，你需要替换成真实的！"
echo ""

# -----------------------------------------------------------
# 第八步：查看远程仓库配置
# -----------------------------------------------------------
echo ">>> 第八步：git remote -v —— 查看远程地址配置"
echo ""

git remote -v

echo ""
echo "   (fetch) = 从哪个地址下载代码"
echo "   (push)  = 向哪个地址推送代码"
echo ""

# -----------------------------------------------------------
# 第九步：尝试推送（会失败，因为地址是假的）
# -----------------------------------------------------------
echo ">>> 第九步：git push -u origin main —— 尝试推送"
echo ""

# 获取当前分支名（可能是 main 也可能是 master）
BRANCH=$(git branch --show-current)

echo "   当前分支：$BRANCH"
echo ""

# 尝试推送（预期会失败，因为占位地址不存在）
set +e  # 暂时允许命令失败
git push -u origin "$BRANCH" 2>&1 || true
set -e

echo ""
echo "   [预期行为] push 失败了，因为 'git@github.com:你的用户名/你的仓库名.git' 这个地址不存在。"
echo "   在实际操作中，你需要先在 GitHub 网页上创建一个真实仓库，"
echo "   然后把上面的占位地址替换成真实地址，推送就能成功。"
echo ""

# -----------------------------------------------------------
# 第十步：完整流程回顾
# -----------------------------------------------------------
echo ">>> 第十步：完整流程回顾"
echo ""

cat << 'STEPS'
   ┌─────────────────────────────────────────────────────────┐
   │  完整操作流程（你需要做的）                              │
   ├─────────────────────────────────────────────────────────┤
   │                                                         │
   │  【GitHub 网页操作】                                     │
   │  ① 登录 GitHub → 右上角 + 号 → New repository          │
   │  ② Repository name 填项目名                             │
   │  ③ 不要勾选 README/.gitignore/license（保持空仓库）     │
   │  ④ 点击 Create repository                               │
   │  ⑤ 在 Quick setup 页面选择 SSH，复制仓库地址             │
   │                                                         │
   │  【本地终端操作】                                        │
   │  ⑥ cd 你的项目目录                                      │
   │  ⑦ git init              （如果还没初始化）              │
   │  ⑧ git add .                                            │
   │  ⑨ git commit -m "初始提交"                             │
   │  ⑩ git remote add origin <复制的SSH地址>                │
   │  ⑪ git push -u origin main     （首次推送）              │
   │  ⑫ 去 GitHub 刷新页面，确认代码出现！                    │
   │                                                         │
   │  【之后每次改代码】                                      │
   │  ⑬ git add .                                            │
   │  ⑭ git commit -m "做了什么修改"                         │
   │  ⑮ git push              （不需要再写 origin main了）   │
   │                                                         │
   └─────────────────────────────────────────────────────────┘
STEPS

echo ""

# -----------------------------------------------------------
# 第十一步：补充说明——如果默认分支是 master
# -----------------------------------------------------------
echo ">>> 补充说明：分支名是 main 还是 master？"
echo ""

echo "   当前仓库的默认分支：$BRANCH"
echo "   GitHub 新仓库默认分支：main"
echo ""
echo "   如果两边不一致，有两个办法："
echo "   ① git branch -M main            （把本地分支改名为 main）"
echo "   ② git push -u origin $BRANCH   （沿用当前分支名推送）"
echo ""

# -----------------------------------------------------------
# 清理
# -----------------------------------------------------------
echo ">>> 清理临时文件"
echo ""

cd /tmp
rm -rf "$DEMO_DIR"

echo "[OK] 演示结束，临时文件已清理。"
echo ""
echo "========================================"
echo "  要真正完成推送，你需要："
echo "  1. 在 GitHub 上创建真实仓库"
echo "  2. 复制 SSH 地址"
echo "  3. 在自己的项目里执行 git remote add + git push"
echo "========================================"
