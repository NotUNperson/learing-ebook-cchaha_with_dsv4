#!/bin/bash
# ============================================================
# 01-SSH密钥配置 - 示例脚本
# 功能：演示 SSH 密钥的生成、查看和连接测试
# 注意：此脚本只做本地操作，不会真的修改你的 .ssh 配置
# ============================================================

set -e  # 遇到错误立即退出

echo "========================================"
echo "  SSH 密钥配置演示脚本"
echo "========================================"
echo ""

# -----------------------------------------------------------
# 第一步：创建临时目录，所有操作都在这里进行
# 不会污染你真正的 ~/.ssh 目录
# -----------------------------------------------------------
DEMO_DIR=$(mktemp -d /tmp/ssh-demo-XXXXXX)
cd "$DEMO_DIR"
echo "[INFO] 演示目录：$DEMO_DIR"
echo "[INFO] 你真正的 .ssh 目录不受影响"
echo ""

# -----------------------------------------------------------
# 第二步：生成 SSH 密钥对
# -----------------------------------------------------------
echo ">>> 第一步：生成 SSH 密钥对"
echo ""

# -t ed25519：使用 Ed25519 算法（现代、安全、快速）
# -f 指定保存的文件名
# -N "" 表示不设密码（空密码）
# -C 后面跟备注（通常放你的邮箱）
ssh-keygen -t ed25519 -f demo_key -N "" -C "demo@example.com"

echo ""
echo "[OK] 密钥生成完毕！"
echo ""

# -----------------------------------------------------------
# 第三步：查看生成的文件
# -----------------------------------------------------------
echo ">>> 第二步：查看生成的密钥文件"
echo ""

ls -la demo_key*    # 星号匹配 demo_key 和 demo_key.pub 两个文件

echo ""
echo "   demo_key      → 私钥（Private Key），像家门钥匙，绝对不能外传"
echo "   demo_key.pub  → 公钥（Public Key），可以放心上传到 GitHub/Gitee"
echo ""

# -----------------------------------------------------------
# 第四步：查看公钥内容
# -----------------------------------------------------------
echo ">>> 第三步：查看公钥内容"
echo ""

cat demo_key.pub

echo ""
echo "   上面这一长串就是你的公钥。你需要把这一段文字"
echo "   粘贴到 GitHub 的 Settings → SSH and GPG keys 页面。"
echo ""

# -----------------------------------------------------------
# 第五步：查看私钥内容（前几行）
# -----------------------------------------------------------
echo ">>> 第四步：查看私钥（仅显示前几行，不会泄露完整内容）"
echo ""

head -n 3 demo_key

echo ""
echo "   注意：私钥以 '-----BEGIN OPENSSH PRIVATE KEY-----' 开头。"
echo "   这个文件绝对不能发给任何人！"
echo ""

# -----------------------------------------------------------
# 第六步：演示公钥的指纹
# -----------------------------------------------------------
echo ">>> 第五步：查看密钥指纹"
echo ""

ssh-keygen -lf demo_key.pub

echo ""
echo "   指纹是公钥的"身份证号"，GitHub 会用指纹来帮你辨认是哪台机器的密钥。"
echo ""

# -----------------------------------------------------------
# 第七步：演示连接验证（仅打印命令，不实际连接）
# -----------------------------------------------------------
echo ">>> 第六步：配置完成后，用以下命令验证连接"
echo ""

echo "   # 测试 GitHub 连接"
echo "   ssh -T git@github.com"
echo ""
echo "   # 测试 Gitee 连接"
echo "   ssh -T git@gitee.com"
echo ""
echo "   如果看到 'successfully authenticated' 就说明配置成功了！"
echo ""

# -----------------------------------------------------------
# 第八步：展示完整的配置流程（仅打印，不实际操作）
# -----------------------------------------------------------
echo ">>> 第七步：完整配置流程回顾（网页操作部分需要手动完成）"
echo ""

cat << 'STEPS'
   完整的 SSH 配置步骤：

   【本机操作（在 Git Bash 中）】
   ① ssh-keygen -t ed25519 -C "your_email@example.com"
   ② cat ~/.ssh/id_ed25519.pub     （查看并复制公钥内容）

   【GitHub 网页操作】
   ③ 登录 GitHub → 右上角头像 → Settings
   ④ 左侧菜单 → SSH and GPG keys
   ⑤ 点击绿色按钮 "New SSH key"
   ⑥ Title 填 "我的Windows电脑"，Key 粘贴公钥内容
   ⑦ 点击 "Add SSH key"

   【验证】
   ⑧ ssh -T git@github.com
   ⑨ 看到 "successfully authenticated" 即完成！

   【Gitee 操作（可选）】
   ⑩ 同 GitHub 流程，进入 Settings → SSH 公钥 即可
STEPS

echo ""

# -----------------------------------------------------------
# 清理
# -----------------------------------------------------------
echo ">>> 清理临时文件"
echo ""

# 回到安全目录
cd /tmp

# 删除演示用的临时目录
rm -rf "$DEMO_DIR"

echo "[OK] 演示结束，临时文件已清理。你的系统没有任何改动。"
echo ""
echo "========================================"
echo "  现在你可以运行真正的命令来配置 SSH："
echo "  ssh-keygen -t ed25519 -C \"你的邮箱\""
echo "========================================"
