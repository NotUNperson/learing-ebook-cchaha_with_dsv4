#!/bin/bash
# ============================================================
# 01-什么是远程仓库 — 示例脚本
# 目标：理解远程仓库的概念、本地与远程的关系
# 说明：本脚本会创建一个临时目录，在本地做提交，然后演示
#        如何查看、添加远程仓库。
# 注意：本脚本只演示本地操作，不涉及真实的远程推送，
#        因为推送需要 GitHub 账号和真实仓库。
# ============================================================

echo "========================================="
echo "  01 - 什么是远程仓库：概念演示脚本"
echo "========================================="
echo ""

# ----------------------------------------------------------
# 步骤1：创建临时练习目录
# ----------------------------------------------------------
TEMP_DIR="/tmp/git-tutorial-remote-intro"
echo ">>> 创建临时练习目录: $TEMP_DIR"
rm -rf "$TEMP_DIR"   # 如果之前练习遗留了这个目录，先删掉
mkdir -p "$TEMP_DIR"
cd "$TEMP_DIR" || exit 1
echo "当前工作在: $(pwd)"
echo ""

# ----------------------------------------------------------
# 步骤2：初始化一个本地 Git 仓库
#         这个仓库现在只有本地，没有远程
# ----------------------------------------------------------
echo ">>> 第1步：初始化一个本地 Git 仓库"
git init my-project
cd my-project || exit 1
echo "初始化完成，当前在: $(pwd)"
echo ""

# ----------------------------------------------------------
# 步骤3：查看远程仓库（应该为空）
#         类比：你刚买了一部新手机，还没登录云相册
# ----------------------------------------------------------
echo ">>> 第2步：查看当前关联的远程仓库"
echo ""
echo "执行: git remote -v"
echo "----------------------------------------"
git remote -v
echo "----------------------------------------"
echo "（没有输出，说明这个仓库还没关联任何远程仓库）"
echo "类比：你手机里刚拍了照，但还没有同步到云相册"
echo ""

# ----------------------------------------------------------
# 步骤4：做一些本地提交
#         类比：你在本地拍了几张照片（做了几个版本）
# ----------------------------------------------------------
echo ">>> 第3步：在本地做一些提交（模拟日常工作）"
echo ""
echo "创建第一个文件..."
echo "print('Hello World')" > main.py
git add main.py
git commit -m "first commit: 添加 main.py"
echo ""

echo "修改一下文件..."
echo "print('Hello Git')" > main.py
git add main.py
git commit -m "second commit: 修改了问候语"
echo ""

echo "再添加一个新文件..."
echo "# 这是一个说明文件" > README.md
git add README.md
git commit -m "third commit: 添加 README"
echo ""

# ----------------------------------------------------------
# 步骤5：查看本地提交历史
#         这些提交目前都在本地，远程仓库里什么都没有
# ----------------------------------------------------------
echo ">>> 第4步：查看本地的提交历史"
echo ""
echo "执行: git log --oneline"
echo "----------------------------------------"
git log --oneline
echo "----------------------------------------"
echo "当前有三个提交，但它们都只存在于你的本机"
echo "类比：你手机里有三张照片，但云相册里是空的"
echo ""

# ----------------------------------------------------------
# 步骤6：模拟添加远程仓库
#         实际使用中 URL 是你 GitHub 仓库的地址
#         这里用一个假地址演示语法
# ----------------------------------------------------------
echo ">>> 第5步：给本地仓库添加一个远程仓库"
echo ""
REMOTE_URL="https://github.com/your-username/my-project.git"
echo "执行: git remote add origin $REMOTE_URL"
git remote add origin "$REMOTE_URL"
echo ""
echo "\"origin\" 是远程仓库的别名，就像一个联系人名字"
echo "你可以叫它 origin、upstream、my-server……"
echo "约定俗成，第一个远程仓库都叫 origin"
echo ""

# ----------------------------------------------------------
# 步骤7：验证远程仓库已添加
# ----------------------------------------------------------
echo ">>> 第6步：再次查看远程仓库"
echo ""
echo "执行: git remote -v"
echo "----------------------------------------"
git remote -v
echo "----------------------------------------"
echo "现在能看到两个条目："
echo "  fetch（拉取）——从哪个地址获取更新"
echo "  push（推送）——把更新推到哪个地址"
echo "这两个地址通常相同"
echo ""

# ----------------------------------------------------------
# 步骤8：查看远程仓库的详细信息
# ----------------------------------------------------------
echo ">>> 第7步：查看远程仓库的更多信息"
echo ""
echo "执行: git remote show origin"
echo "----------------------------------------"
git remote show origin
echo "----------------------------------------"
echo ""

# ----------------------------------------------------------
# 步骤9：解释概念——本地 vs 远程
# ----------------------------------------------------------
echo "========================================="
echo "  概念总结"
echo "========================================="
echo ""
echo "  本地仓库（你的电脑）          远程仓库（服务器）"
echo "  ┌─────────────────┐          ┌─────────────────┐"
echo "  │  .git 目录       │  push   │  和你一样的     │"
echo "  │  所有提交记录    │ ======> │  .git 目录      │"
echo "  │  所有分支        │  <===== │  所有提交记录    │"
echo "  │  当前工作文件    │   pull  │  所有分支        │"
echo "  └─────────────────┘          └─────────────────┘"
echo ""
echo "  关键理解："
echo "  1. 本地操作（commit、branch）不会自动影响远程"
echo "  2. push = 把本地的提交上传到远程"
echo "  3. pull = 从远程下载新的提交到本地"
echo "  4. 类比：云相册——你拍的照片在手机（本地），"
echo "            同步后也在云端（远程）"
echo ""

# ----------------------------------------------------------
# 清理提示
# ----------------------------------------------------------
echo "========================================="
echo "  练习完成！"
echo "========================================="
echo ""
echo "本脚本的练习仓库在: $TEMP_DIR/my-project"
echo "你可以:"
echo "  1. cd $TEMP_DIR/my-project  进入仓库看看"
echo "  2. 执行 git log 查看提交历史"
echo "  3. 执行 git remote -v 查看远程仓库"
echo ""
echo "想清理的话，执行: rm -rf $TEMP_DIR"
echo ""
echo "下一节: 02-克隆仓库深入"
