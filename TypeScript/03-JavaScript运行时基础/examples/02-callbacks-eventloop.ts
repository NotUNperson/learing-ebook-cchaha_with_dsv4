// [同步] 程序开始
// [同步] 程序结束
// [异步] 延迟 0ms       ← 虽然延迟最短，但在同步代码之后
// [异步] 延迟 200ms
// [异步] 延迟 500ms
// 事件循环按到期时间顺序从队列取出回调执行
