// ============================================================
// 第一部分：回调函数的基本概念
// ============================================================

/**
 * 回调函数（callback）就是一个被当作参数传入另一个函数的函数。
 * 被调用方在合适的时机（比如任务完成时）调用这个函数。
 *
 * 类比：你告诉朋友"水开了叫我一声"。
 * "叫我一声"就是回调函数，你朋友（被调用方）在水开时调用它。
 */

// 定义一个函数，它接受一个回调函数作为参数
function doHomework(subject: string, callback: () => void): void {
  console.log(`[主线程] 开始做${subject}作业...`);

  // setTimeout 模拟"做作业需要时间"（异步操作）
  setTimeout(() => {
    console.log(`[定时器] ${subject}作业做完了！`);
    callback(); // 做完后调用回调，通知外面的人
  }, 2000);
}

console.log("=== 第一部分：回调函数演示 ===");

// 调用 doHomework，传入一个回调函数
// 这个回调函数就是"做完作业之后要做的事"
doHomework("数学", () => {
  console.log("[回调] 收到通知：可以休息了！");
});

console.log("[主线程] doHomework 调用已返回，我去看会电视...");
// 注意：这行会先于"数学作业做完了"打印出来
// 因为 setTimeout 是非阻塞的，2000ms 后才执行回调

// ============================================================
// 第二部分：setTimeout 详解
// ============================================================

console.log("\n=== 第二部分：setTimeout 执行顺序 ===");

// setTimeout 的三个特点：
// 1. 非阻塞 —— 不会等待延迟结束，立刻返回
// 2. 回调排队 —— 延迟到了，回调不是立刻执行，而是排入"任务队列"
// 3. 最小延迟 —— 即使指定 0ms，回调也要等当前代码执行完

console.log("1. 第一行");

setTimeout(() => {
  console.log("4. setTimeout 回调（延迟 0ms）");
}, 0);

setTimeout(() => {
  console.log("3. setTimeout 回调（延迟 100ms）");
}, 100);

console.log("2. 第二行");

// 输出顺序（分析）：
// 1 → 2 → 3/4（取决于哪个先到期，但都在 1 和 2 之后）
// 实际上：1 → 2 → 4 → 3（因为 0ms 的虽然延迟短，也要在 2 之后）

// 等一会儿，让所有定时器回调都执行完，再继续下一部分
setTimeout(() => {
  // ============================================================
  // 第三部分：回调地狱 —— 嵌套回调的可读性问题
  // ============================================================

  console.log("\n=== 第三部分：回调地狱 ===");

  // 假设我们有三个步骤，每一步都依赖上一步的结果
  // 步骤 1：洗菜（1 秒） → 步骤 2：切菜（1 秒） → 步骤 3：炒菜（1 秒）

  function washVegetables(callback: (name: string) => void): void {
    setTimeout(() => {
      console.log("  1. 菜洗好了");
      callback("洗干净的白菜");
    }, 1000);
  }

  function cutVegetables(veg: string, callback: (result: string) => void): void {
    setTimeout(() => {
      console.log(`  2. ${veg} 切好了`);
      callback("切好的白菜");
    }, 1000);
  }

  function cookVegetables(veg: string, callback: (result: string) => void): void {
    setTimeout(() => {
      console.log(`  3. ${veg} 炒好了`);
      callback("一盘美味的炒白菜");
    }, 1000);
  }

  console.log("开始做菜（回调地狱版本）：");

  // 看，代码一层层往右缩进去 —— 这就是"回调地狱"
  washVegetables((cleanVeg: string) => {
    cutVegetables(cleanVeg, (cutVeg: string) => {
      cookVegetables(cutVeg, (dish: string) => {
        console.log(`  完成！上菜：${dish}`);
      });
    });
  });

  console.log("做菜过程中，厨师还可以干别的事...");

}, 300); // 等 300ms，让前面的定时器输出完

// ============================================================
// 第四部分：事件循环的可视化理解
// ============================================================

console.log("\n=== 第四部分：事件循环模型 ===");

/**
 * 餐厅点餐模型：
 *
 *   服务员（主线程）    厨房（异步后台）    出餐口（任务队列）
 *   ──────────────     ──────────────     ──────────────
 *   1. 记单            接单开始做菜
 *   2. 服务下一位                        菜做好了 →
 *   3. 有空了                              ← 菜在出餐口
 *   4. 端给客人
 *
 * 事件循环 = 服务员不断检查"出餐口有没有新做好的菜"的循环
 *
 * while (true) {
 *   1. 检查任务队列有没有待执行的回调
 *   2. 有的话，取出一个执行
 *   3. 重复
 * }
 */

// 模拟事件循环的简化伪代码（不会真的运行，只是帮助你理解）：
const eventLoopExplanation: string = `
┌──────────────────────────────────────┐
│          事件循环（Event Loop）       │
│                                      │
│   主线程（单线程）                     │
│   ↓                                  │
│   [执行同步代码]                      │
│   ↓                                  │
│   [检查任务队列] ← 回调在这里排队      │
│   ↓          ↑                       │
│   [有空就取一个执行] ────────────────→ │
│                                      │
│   异步操作（在后台）：                  │
│   - 定时器到期                        │
│   - 文件读取完成                      │
│   - 网络请求返回                      │
│      ↓                               │
│   自动放入任务队列                     │
└──────────────────────────────────────┘
`;

console.log(eventLoopExplanation);

