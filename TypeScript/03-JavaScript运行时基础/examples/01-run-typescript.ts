// ============================================================
// 第四部分：对比 —— C++ vs TypeScript 的编译流程
// ============================================================

// C++ 流程（你熟悉的）：
//   源代码 .cpp  --[g++编译]--> 可执行文件 .exe  --[操作系统]--> 运行
//   一步编译成机器码，然后原生执行

// TypeScript 流程：
//   源代码 .ts  --[tsc编译]--> JavaScript .js  --[Node.js(V8)]--> 运行
//   先脱掉类型变成 JS，再交给 JS 引擎执行
//   多了一层，但换来了跨平台 —— 同一份 .js 在 Windows/Mac/Linux 都能跑

// ts-node 的流程（我们本课程使用的方式）：
//   源代码 .ts  --[ts-node(内部先 tsc 再 node)]--> 直接看到输出
//   把编译和运行两步合成一步，方便学习和开发

// ============================================================
// 第五部分：观察编译结果
// ============================================================

// 给程序一个显式的结尾。如果上面代码跑通了，你会看到这行：
console.log("\n程序运行完毕！");

