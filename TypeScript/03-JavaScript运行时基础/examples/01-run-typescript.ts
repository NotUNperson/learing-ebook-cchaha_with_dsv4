/**
 * 01-run-typescript.ts
 * 你的第一个 TypeScript 程序 —— 理解 TS 如何运行
 *
 * 运行方式：
 *   ts-node 01-run-typescript.ts
 * 或者：
 *   tsc 01-run-typescript.ts    # 编译成 01-run-typescript.js
 *   node 01-run-typescript.js   # 执行编译后的 JS
 */

// ============================================================
// 第一部分：基本类型声明和输出
// ============================================================

// 类型注解：告诉 TypeScript 这个变量只能放字符串
// 如果你试图给它赋数字，tsc 会直接报错（在编译阶段就拦住你）
const greeting: string = "你好，TypeScript！";

// console.log 是 Node.js 提供的全局 API
// 作用是把文本打印到终端（类似 C++ 的 std::cout）
console.log(greeting);

// ============================================================
// 第二部分：在 TS 里做简单计算
// ============================================================

// 数字类型 number：TypeScript 不区分 int / float / double
// 所有数字，不管是整数还是小数，都是 number
const price: number = 99.5;
const quantity: number = 3;
const total: number = price * quantity;

// 模板字符串：用反引号 ` ` 包裹，${} 里放变量或表达式
// 相当于 C++ 的 std::format 或 printf 的 %d
console.log(`单价: ${price}, 数量: ${quantity}, 总价: ${total}`);

// ============================================================
// 第三部分：演示类型检查的作用
// ============================================================

function add(a: number, b: number): number {
  // 参数和返回值都标注了 number 类型
  // 如果你传入字符串，TS 在编译时就会提醒你
  return a + b;
}

// 正确调用
const result: number = add(10, 25);
console.log(`add(10, 25) = ${result}`);

