# ================================================================
# 1. 浏览器-服务器模型（打电话叫外卖）
# ================================================================
#
# 你和网站的互动，就像打电话叫外卖：
#
#   你（浏览器）  拨号  ──────────>  餐厅（服务器）
#   "我要一份宫保鸡丁"  (请求/Request)
#
#   你（浏览器）  <──────────  餐厅（服务器）
#   "好的，30分钟内送到"  (响应/Response)
#
# 每一步的具体对应：
#   - 你在浏览器地址栏输入网址并回车 = 拨通餐厅电话
#   - 浏览器向服务器发送请求 = 你向服务员点菜
#   - 服务器处理请求、生成页面 = 厨房做菜
#   - 服务器返回 HTML 页面给浏览器 = 外卖小哥送餐到你家
#   - 浏览器渲染显示页面 = 你打开餐盒开吃

# ================================================================
# 2. 什么是框架
# ================================================================
#
# 不靠框架，自己从零写一个网站：
#   你得自己处理：怎么接收 HTTP 请求？怎么解析 URL？
#   怎么连接数据库？怎么防止安全问题？怎么管理用户登录？
#   ...就像你从种小麦开始做一碗面条。
#
# 有了框架（Framework）：
#   框架帮你做好了所有的"基础建设"。你只需要：
#     1. 定义 URL 规则（用户访问哪个网址能看到什么）
#     2. 写视图函数（每个页面具体显示什么内容）
#     3. 设计数据模型（你的网站要存储什么数据）
#   框架会帮你把剩下的一切都搞定。

# ================================================================
# 3. Django 的特点 —— 自带全套"家电"
# ================================================================
#
# Django 和其他 Python Web 框架最大的不同：
# Django 是"电池全含"（batteries included）
#
# 类比：
#   学 Flask（另一个 Python Web 框架）= 租一个空房子
#     你需要自己买冰箱、洗衣机、微波炉...
#     灵活性高，但什么都要自己动手
#
#   学 Django = 买一套精装修房
#     冰箱（ORM 数据库工具）、洗衣机（表单处理）、微波炉（管理后台）
#     全套配齐，拎包入住
#     虽然不如 Flask 灵活，但对新手极其友好
#
# Django 自带的"家电"包括：
#   - ORM（对象关系映射）：用 Python 代码操作数据库，不用写 SQL
#   - 模板引擎：把数据动态插入 HTML 页面
#   - 表单系统：自动生成表单、验证用户输入
#   - Admin 后台：自动生成一个功能齐全的数据管理界面
#   - 用户认证系统：注册、登录、权限管理
#   - 安全防护：SQL 注入防护、CSRF 防护、XSS 防护

# ================================================================
# 4. Django 的 MTV 架构模式
# ================================================================
#
# Django 采用 MTV 架构（Model-Template-View）：
#
#   Model（模型） = 餐厅的"食材仓库"
#     管理数据：有哪些食材、每种食材有多少库存
#     对应数据库中的表结构
#
#   Template（模板） = 餐厅的"菜单样式"
#     决定页面的外观：布局、颜色、字体
#     是 HTML 文件，但可以嵌入动态数据
#
#   View（视图） = 餐厅的"厨师"
#     接收请求（顾客的点单），处理逻辑（烹饪），
#     返回响应（上菜）
#     连接 Model 和 Template 的桥梁
#
#   URL 路由 = 餐厅的"菜单"
#     顾客说"我要 3 号套餐" → 对应到某个厨师去做某道菜
#
# 一个典型的请求处理流程：
#   1. 用户访问 https://你的网站.com/posts/
#   2. URL 路由匹配到 /posts/ → 找到对应的视图函数
#   3. 视图函数从 Model 获取所有文章数据
#   4. 视图函数把数据传给 Template，生成 HTML
#   5. HTML 返回给用户的浏览器

