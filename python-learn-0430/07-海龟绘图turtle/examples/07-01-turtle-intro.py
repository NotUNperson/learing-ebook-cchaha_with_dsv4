# -*- coding: utf-8 -*-
"""
07-01 turtle 入门示例
====================
本节介绍 turtle 库的基本概念：创建画布、创建海龟、前进后退。
运行此脚本会打开一个窗口，小海龟会移动并留下一段线条。

类比：就像用遥控器操控一辆会留下轨迹的小汽车。
"""

import turtle

# ---- 1. 创建画布（就像在地上铺一张白纸） ----
window = turtle.Screen()
window.title("我的第一幅 turtle 画")       # 窗口标题
window.bgcolor("white")                   # 背景颜色：白色

# ---- 2. 创建小海龟（拿到遥控器） ----
t = turtle.Turtle()          # 创建 Turtle 对象，命名为 t
t.shape("turtle")            # 把默认的箭头改成海龟形状
t.color("blue")              # 设置画线的颜色为蓝色

# ---- 3. 发出移动指令 ----
t.forward(100)   # 前进 100 像素（海龟初始头朝右）
t.backward(50)   # 后退 50 像素（头还是朝右，只是退着走）

# ---- 4. 保持窗口，直到用户手动关闭 ----
turtle.done()
# 没有这行的话窗口会一闪而过，什么都看不到
