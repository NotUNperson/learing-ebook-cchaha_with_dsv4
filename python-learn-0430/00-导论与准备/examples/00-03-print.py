"""
============================================================
00-03 print() 的用法演示
============================================================
print() 是最基础的输出函数，让计算机在屏幕上显示内容。
"""

# 1. 打印文字——用引号包起来
print("你好，世界！")
print('Python 很好学')          # 单引号也可以
print("2024年，新开始")         # 中文完全没问题

print("-" * 30)  # 分隔线

# 2. 打印数字——不需要引号
print(100)
print(3.14159)
print(50 + 30)                  # 计算机会先算 50+30=80，再打印 80
print(10 * 5)                   # 10*5=50

print("-" * 30)  # 分隔线

# 3. 一次打印多个内容——用逗号分隔
print("我叫", "小明", "今年", 10, "岁")
# 输出：我叫 小明 今年 10 岁
# 注意：逗号之间会自动加一个空格

print("-" * 30)  # 分隔线

# 4. 混合文字和算式——引号里的原样输出，没有引号的先算再输出
print("一年有", 365 * 24, "小时")
print("一小时有", 60 * 60, "秒")

# 5. 打印空行
print()  # 什么都不写，就打印一个空行
print("上面空了一行，注意到了吗？")
