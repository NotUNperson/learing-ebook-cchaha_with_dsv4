# 导入 sys 模块——sys 是"system（系统）"的缩写
# 它可以告诉我们当前 Python 的版本信息
import sys

# print() 是最基本的输出命令，让计算机在屏幕上显示内容
print("正在检查 Python 安装...")
print("-" * 40)  # 打印 40 个短横线作为分隔线

# sys.version 显示当前 Python 的详细版本号
print("Python 版本：", sys.version)

# 空一行，让输出更好看
print()

# 打印成功信息
print("安装成功！")

print("-" * 40)
print("你现在可以开始学习 Python 了！")
