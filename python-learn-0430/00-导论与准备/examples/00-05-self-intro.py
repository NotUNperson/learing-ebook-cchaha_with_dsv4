"""
============================================================
00-05 综合练习：自我介绍程序
============================================================
综合运用 print() 和变量，做一个自我介绍的小程序。
"""

# ========== 第 1 步：存放个人信息 ==========
name = "小明"            # 名字——字符串用引号
age = 10                  # 年龄——数字不用引号
city = "北京"            # 城市
hobby = "打篮球"         # 爱好
favorite_food = "红烧肉"  # 喜欢的食物
dream = "当宇航员"        # 梦想

# 计算出生年份
current_year = 2024
birth_year = current_year - age

# ========== 第 2 步：打印自我介绍 ==========
print("=" * 30)           # 打印分隔线
print("我的自我介绍")
print("=" * 30)

print("大家好！")
print("我叫", name)
print("今年", age, "岁")
print("我来自", city)
print("我的爱好是", hobby)
print("我爱吃", favorite_food)
print("我的梦想是", dream)
print("我出生于", birth_year, "年")

print("-" * 30)
print("很高兴认识大家！请多多指教。")
print("=" * 30)

# ========== 第 3 步：再加点互动信息 ==========
print()  # 空行
print("你知道吗？")
print("假设我活到 80 岁，那我还剩", 80 - age, "年可以做自己喜欢的事！")
print("每天进步一点点，加油！")
