"""
============================================================
01-01 数字与算术运算
============================================================
Python 中数字分两种：int（整数）和 float（浮点数/小数）。
加减乘除和数学课一样，还有取余（%）和幂运算（**）。
"""

print("========== 基本运算 ==========")

# 加减乘除
print("8 + 5 =", 8 + 5)        # 加法
print("100 - 87 =", 100 - 87)  # 减法
print("6 * 3 =", 6 * 3)        # 乘法
print("20 / 4 =", 20 / 4)      # 除法——注意结果是浮点数 5.0

print("\n========== 取余运算 % ==========")
# 取余：除法之后的"余数"
print("10 ÷ 3 的余数是", 10 % 3)   # 10 = 3*3 + 1，余 1
print("17 ÷ 5 的余数是", 17 % 5)   # 17 = 5*3 + 2，余 2
print("8 ÷ 2 的余数是", 8 % 2)     # 8 = 2*4 + 0，整除，余 0

# 取余的实用场景：判断奇偶数
print("7 是奇数吗？", 7 % 2 == 1)   # 如果 7%2 余 1，就是奇数
print("8 是偶数吗？", 8 % 2 == 0)   # 如果 8%2 余 0，就是偶数

print("\n========== 幂运算 ** ==========")
print("2 的 3 次方 =", 2 ** 3)     # 2*2*2 = 8
print("10 的平方 =", 10 ** 2)      # 10*10 = 100
print("3 的 4 次方 =", 3 ** 4)     # 3*3*3*3 = 81

print("\n========== 运算顺序 ==========")
print("10 + 2 * 3 =", 10 + 2 * 3)      # 先乘后加：2*3=6, 10+6=16
print("(10 + 2) * 3 =", (10 + 2) * 3)  # 先括号：10+2=12, 12*3=36
# 结论：不确定时加括号最安全！

print("\n========== 整数和浮点数混用 ==========")
print("10 + 2.5 =", 10 + 2.5)      # 整数 + 浮点数 = 浮点数
print("5 * 2.0 =", 5 * 2.0)        # 结果也是浮点数

print("\n========== 生活中的算术 ==========")
# 一年有多少秒？
seconds_per_year = 365 * 24 * 60 * 60
print("一年有", seconds_per_year, "秒")

# 购物结算
budget = 100        # 预算
book_price = 28     # 每本书价格
buy_count = 3       # 买几本
remain = budget - book_price * buy_count
print("100 元买 3 本书（每本 28 元），还剩", remain, "元")
