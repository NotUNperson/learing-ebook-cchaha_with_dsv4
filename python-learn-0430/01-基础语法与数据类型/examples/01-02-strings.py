"""
============================================================
01-02 字符串基础
============================================================
字符串 = 用引号包起来的一串文字。
可以用 + 拼接，用 * 重复。
"""

# 1. 创建字符串——单引号或双引号都行
print("这是用双引号创建的字符串")
print('这是用单引号创建的字符串')
print("中英文混合 Hello 也完全 OK")

print("-" * 30)

# 2. 引号的"俄罗斯套娃"——外层和内层用不同的引号
print("小明说：'今天天气真好！'")    # 双引号套单引号
print('小明说："今天天气真好！"')    # 单引号套双引号

print("-" * 30)

# 3. 字符串拼接——用 + 号把文字粘在一起
first = "张"
last = "小明"
full_name = first + last          # 拼接姓和名
print("全名：", full_name)

greeting = "你好，" + "欢迎来到 Python 的世界！"
print(greeting)

# 注意：+ 拼接是"直接粘"，不会自动加空格
print("hello" + "world")          # 输出 helloworld（没有空格）
print("hello" + " " + "world")    # 输出 hello world（手动加空格）

print("-" * 30)

# 4. 字符串重复——用 * 号
print("重要！" * 5)               # 重复 5 遍
print("=" * 40)                   # 用来做分隔线特别好用
print("哈" * 10)                  # 十个哈

print("-" * 30)

# 5. 字符串和变量的区别（重要！）
name = "小明"                     # name 是变量，"小明" 是字符串
print(name)                       # 打印变量的值：小明
print("name")                     # 打印字符串 "name"：name
# 看到区别了吗？有引号就是"原样输出"，没引号就是"找变量"

# 6. 数字不能直接拼字符串——要用逗号
# 错误写法（取消注释会报错）：
# print("我有 " + 10 + " 本书")
# 正确写法：
print("我有", 10, "本书")         # 用逗号分隔，OK
