"""
============================================================
01-05 列表入门
============================================================
列表 = 一个有序的容器，像购物清单。
用 [] 创建，索引从 0 开始，可以修改内容。
"""

# 1. 创建列表
shopping_list = ["牛奶", "面包", "鸡蛋", "香蕉"]
print("购物清单：", shopping_list)

numbers = [10, 20, 30, 40, 50]          # 全是数字的列表
mixed = ["小明", 10, 165.5, True]       # 混合类型的列表
empty = []                               # 空列表

print("数字列表：", numbers)
print("混合列表：", mixed)
print("空列表：", empty)

print("-" * 30)

# 2. 索引访问 —— 从 0 开始！
fruits = ["苹果", "香蕉", "橘子", "葡萄"]
print("水果列表：", fruits)
print("索引 0（第1个）：", fruits[0])    # 苹果
print("索引 1（第2个）：", fruits[1])    # 香蕉
print("索引 2（第3个）：", fruits[2])    # 橘子
print("索引 3（第4个）：", fruits[3])    # 葡萄

print("-" * 10)
# 负索引：倒着数
print("索引 -1（最后1个）：", fruits[-1])   # 葡萄
print("索引 -2（倒数第2）：", fruits[-2])   # 橘子

print("-" * 30)

# 3. len() —— 列表的长度
print("水果列表有", len(fruits), "项")
print("空列表有", len(empty), "项")

print("-" * 30)

# 4. 列表元素可以修改（和字符串不同！）
scores = [85, 90, 78]
print("原始成绩：", scores)
scores[1] = 95        # 把索引 1（第 2 个）改成 95
print("修改后：", scores)

# 对比：字符串不能这样改
# word = "hello"
# word[0] = "H"  # 这会报错！字符串不可修改

print("-" * 30)

# 5. 列表里可以放列表（嵌套）
nested = [["苹果", "香蕉"], ["牛奶", "面包"]]
print("嵌套列表：", nested)
print("第一组（索引0）：", nested[0])       # ['苹果', '香蕉']
print("第一组的第一个：", nested[0][0])     # 苹果
# 这就像在一个大袋子里又装了两个小袋子
