// ============================================================
// 1. 算术运算符
// ============================================================

console.log("========== 算术运算符 ==========");

let a = 10;
let b = 3;

console.log("a =", a, ", b =", b);
console.log("a + b =", a + b);      // 13
console.log("a - b =", a - b);      // 7
console.log("a * b =", a * b);      // 30
console.log("a / b =", a / b);      // 3.3333333333333335  ← 注意：结果是小数！
console.log("a % b =", a % b);      // 1  (10 ÷ 3 = 3 余 1)
console.log("a ** b =", a ** b);    // 1000  (10 的 3 次方)

// C 语言没有的：幂运算符 **
console.log("\n幂运算示例：");
console.log("2 ** 10 =", 2 ** 10);  // 1024
console.log("3 ** 4 =", 3 ** 4);    // 81

// JS 除法永远是浮点数
console.log("\n除法结果永远是小数：");
console.log("10 / 3 =", 10 / 3);    // 3.3333333333333335
console.log("5 / 2 =", 5 / 2);      // 2.5  （C 语言中 5/2 = 2）
console.log("7 / 2 =", 7 / 2);      // 3.5

// 想要整数结果？用 Math 函数
console.log("\n如何获得整数除法的效果：");
console.log("Math.floor(10 / 3) =", Math.floor(10 / 3));   // 3（向下取整）
console.log("Math.trunc(10 / 3) =", Math.trunc(10 / 3));   // 3（截断小数）
console.log("Math.ceil(10 / 3) =", Math.ceil(10 / 3));     // 4（向上取整）
console.log("Math.round(10 / 3) =", Math.round(10 / 3));   // 3（四舍五入）

// % 取余（包括负数）
console.log("\n取余运算：");
console.log("10 % 3 =", 10 % 3);    // 1
console.log("-10 % 3 =", -10 % 3);  // -1  -- 余数带符号
console.log("10 % -3 =", 10 % -3);  // 1

// ============================================================
// 2. 赋值运算符
// ============================================================

console.log("\n========== 赋值运算符 ==========");

let x = 10;
console.log("初始 x =", x);

x += 5;   // x = x + 5
console.log("x += 5:", x);  // 15

x -= 3;   // x = x - 3
console.log("x -= 3:", x);  // 12

x *= 2;   // x = x * 2
console.log("x *= 2:", x);  // 24

x /= 4;   // x = x / 4
console.log("x /= 4:", x);  // 6

x %= 4;   // x = x % 4
console.log("x %= 4:", x);  // 2

x **= 3;  // x = x ** 3
console.log("x **= 3:", x); // 8

// ============================================================
// 3. 自增自减：++ 和 --
// ============================================================

console.log("\n========== 自增自减 ==========");

// 后缀：先用后加
let n = 5;
let postResult = n++;
console.log("后缀：n++");
console.log("  postResult =", postResult);  // 5（先把 n 的旧值给了 postResult）
console.log("  n =", n);                       // 6（然后 n 自增）

// 前缀：先加后用
let m = 5;
let preResult = ++m;
console.log("前缀：++m");
console.log("  preResult =", preResult);  // 6（先自增，再把新值给 preResult）
console.log("  m =", m);                   // 6

// 自减同理
let p = 5;
console.log("p-- =", p--, "  p =", p);  // 输出 5  然后 p=4
let q = 5;
console.log("--q =", --q, "  q =", q);  // 输出 4  然后 q=4

// ============================================================
// 4. + 的双面性：加法 vs 拼接
// ============================================================

console.log("\n========== + 的双面性 ==========");

console.log("10 + 20 =", 10 + 20);              // 30 -- 加法
console.log('"10" + "20" =', "10" + "20");      // "1020" -- 拼接
console.log('"10" + 20 =', "10" + 20);          // "1020" -- 混合，数字转字符串

// 计算顺序影响结果！
console.log("\n计算顺序的影响：");
console.log('10 + 20 + "30" =', 10 + 20 + "30");     // "3030"
// 过程：10 + 20 = 30, 然后 30 + "30" = "3030"

console.log('"10" + 20 + 30 =', "10" + 20 + 30);     // "102030"
// 过程："10" + 20 = "1020", 然后 "1020" + 30 = "102030"

console.log('10 + "20" + 30 =', 10 + "20" + 30);     // "102030"
// 过程：10 + "20" = "1020", 然后 "1020" + 30 = "102030"

// 和其他运算符对比
console.log("\n其他运算符都会尝试把字符串转数字：");
console.log('"6" - 2 =', "6" - 2);     // 4
console.log('"6" * "2" =', "6" * "2"); // 12
console.log('"6" / "2" =', "6" / "2"); // 3

// ============================================================
// 5. 运算符优先级
// ============================================================

console.log("\n========== 运算符优先级 ==========");

// 先乘除后加减
console.log("2 + 3 * 4 =", 2 + 3 * 4);        // 14，不是 20

// 括号改变优先级
console.log("(2 + 3) * 4 =", (2 + 3) * 4);    // 20

// 复杂的表达式用括号更清晰
let height = 10;
let width = 20;
let depth = 5;
let volume = height * width * depth;           // 体积
let surfaceArea = 2 * (height * width + width * depth + height * depth);
console.log("体积:", volume);
console.log("表面积:", surfaceArea);

// 赋值运算符优先级很低
let y = 5;
let z = y = 10;        // 先算 y = 10，再算 z = y
console.log("y =", y, "z =", z);  // y=10, z=10

// ============================================================
// 小结：
// - 除法永远返回浮点数，和 C 不同
// - ** 是幂运算，C 没有
// - ++/-- 前后缀和 C 一样
// - + 遇到字符串变拼接
// - 优先级记不住就用括号
// ============================================================
