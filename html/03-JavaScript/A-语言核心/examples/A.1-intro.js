// ============================================================
// 1. 最基础：打印输出
// console.log() 是 JS 最常用的输出方式，类似 C 的 printf
// ============================================================

console.log("Hello, World!");
console.log("JavaScript，我来啦！");

// 分号在 JS 中是可选的（但推荐加上，避免歧义）
console.log("这一行有分号");
console.log("这一行也有分号");

// ============================================================
// 2. JavaScript 可以当计算器用
// Node.js 会直接执行并输出结果
// ============================================================

console.log(1 + 2);        // 输出: 3
console.log(100 - 37);     // 输出: 63
console.log(8 * 7);        // 输出: 56
console.log(100 / 3);      // 输出: 33.333333333333336  （注意：JS 除法结果是小数！）
console.log(10 % 3);       // 输出: 1  取余运算

// ============================================================
// 3. 可以在一条语句中拼接多个值
// 用逗号分隔，console.log 会依次打印
// ============================================================

console.log("1 + 2 =", 1 + 2);
console.log("你好，", "世界！");

// ============================================================
// 4. 关于注释
// 单行注释用 //
// 多行注释用 /* ... */ （和 C 语言一模一样）
// ============================================================

/* 这是一个多行注释
   可以写很多行
   和 C 语言的注释完全一样 */

